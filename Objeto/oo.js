//Paradigma baseado em procedimentos/funções que manipulam dados
processamento(valor1, valor2, valor3);

//Orientação a Objetos
//Paradigma baseado em dados que contém procedimentos/funções
//O objeto é como uma cápsula que agrupa comportamentos e atributos
//O foco passou a ser o objeto
objeto = {
    valor1,
    valor2,
    valor3,
    processamento() {
        //...
    }
}

//A função passa a ser chamada a partir do objeto
objeto.processamento();

//Princípios importantes
//1. Abstração - Simplificar e organizar algo de forma que os elementos desnecessários sejam deixados de lado,
//para que esse algo possa ser entendido de forma mais fácil e clara para determinado propósito.
//2. Encapsulamento - É a ocultação dos detalhes de implementação de um código, de forma que seja apresentado para quem precise usar aquele sistema ou coisa,
//uma interface simples para que se possa interagir com determinado objeto.
//3. Herança (prototype) - O objeto recebe atributos e comportamentos de um objeto pai do qual o filho deriva.
//4. Polimorfismo - Significa múltiplas formas. É a derivação de um tipo ou objeto mais genérico, para um mais específico, o qual pode herdar atributos e comportamentos
